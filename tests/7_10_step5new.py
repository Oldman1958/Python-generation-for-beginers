solutions = []

# –ü–µ—Ä–µ–±–∏—Ä–∞–µ–º –≤—Å–µ –≤–æ–∑–º–æ–∂–Ω—ã–µ –∑–Ω–∞—á–µ–Ω–∏—è –¥–ª—è üçå, üíé –∏ ü¶å –≤ –¥–∏–∞–ø–∞–∑–æ–Ω–µ [1, n)
