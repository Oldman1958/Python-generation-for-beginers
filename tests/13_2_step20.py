"""
Посчитай количества 🔢🌶️
Напишите функцию print_symbol_counts(s),
которая принимает на вход слово s и выводит для каждой буквы этого слова в лексикографическом порядке
в нижнем регистре на отдельной строке количество её вхождений в это слово в следующем формате:

<L>: <N>

где <L> – некоторая буква слова s, <N> – количество вхождений этой буквы в слово s.

Примечание.
Обратите внимание: если в слове встречается одна и та же буква в разных регистрах,
то мы считаем это одной и той же буквой (см. тест №4).
"""


# объявление функции
def print_symbol_counts(s):
    # Приводим все буквы к нижнему регистру
    s_lower = s.lower()

    # Создаём словарь для подсчёта вхождений каждой буквы
    counts = {}
    for char in s_lower:
        if char.isalpha():  # Учитываем только буквы
            counts[char] = counts.get(char, 0) + 1

    # Сортируем буквы в лексикографическом порядке
    sorted_letters = sorted(counts.keys())

    # Выводим результат в требуемом формате
    for letter in sorted_letters:
        print(f"{letter}: {counts[letter]}")


# считываем данные
s = input()

# вызываем функцию
print_symbol_counts(s)
