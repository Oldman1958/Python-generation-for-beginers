"""
Напишите программу, которая считывает последовательность из 10 целых чисел и определяет,
является ли каждое из них чётным или нет.

Формат входных данных
На вход программе подаются 10 целых чисел, каждое на отдельной строке.

Формат выходных данных
Программа должна вывести текст «YES» (без кавычек), если все числа чётные, или «NO» (без кавычек) в противном случае.
"""

for i in range(10):
    a = int(input())
    if a % 2 != 0:
        print('NO')
        exit()
print('YES')
