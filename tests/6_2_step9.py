"""
Вводятся 3 строки в случайном порядке.
Напишите программу, которая выясняет, можно ли из длин этих строк построить арифметическую прогрессию.

Формат входных данных
На вход программе подаются три строки, каждая на отдельной строке.

Формат выходных данных
Программа должна вывести строку «YES» (без кавычек),
если из длин введённых слов можно построить арифметическую прогрессию, или «NO» (без кавычек) в противном случае.
"""

s1 = input()
s2 = input()
s3 = input()
ls1 = len(s1)
ls2 = len(s2)
ls3 = len(s3)
sum = ls1 + ls2 + ls3
middle = sum - min(ls1, ls2, ls3) - max(ls1, ls2, ls3)
dif1 = max(ls1, ls2, ls3) - middle
dif2 = middle - min(ls1, ls2, ls3)
print('YES' if dif1 == dif2 else 'NO')
