"""
Дано натуральное число n(n>99). Напишите программу, которая определяет его третью (с начала) цифру.

Формат входных данных
На вход программе подаётся одно натуральное число, состоящее как минимум из трёх цифр.

Формат выходных данных
Программа должна вывести его третью (с начала) цифру.
"""

n = int(input())
last_digit = 0
while len(str(n)) > 2:
    last_digit = n % 10
    n //= 10
print(last_digit)
