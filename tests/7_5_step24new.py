"""
–í—Ä–µ–º–µ–Ω–Ω–æ–π –ø—Ä–æ–º–µ–∂—É—Ç–æ–∫ ‚è±Ô∏èüå∂Ô∏è
–ù–∞ –≤—Ö–æ–¥ –ø—Ä–æ–≥—Ä–∞–º–º–µ –ø–æ–¥–∞—é—Ç—Å—è —á–µ—Ç—ã—Ä–µ —Ü–µ–ª—ã—Ö —á–∏—Å–ª–∞ h1,m1,h2,m2, –∫–æ—Ç–æ—Ä—ã–µ –∑–∞–¥–∞—é—Ç –≤—Ä–µ–º–µ–Ω–Ω–æ–π –ø—Ä–æ–º–µ–∂—É—Ç–æ–∫ –æ—Ç h‚ÇÅ:m‚ÇÅ –¥–æ h‚ÇÇ:m‚ÇÇ.
–ù–∞–ø–∏—à–∏—Ç–µ –ø—Ä–æ–≥—Ä–∞–º–º—É, –∫–æ—Ç–æ—Ä–∞—è –≤—ã–≤–æ–¥–∏—Ç –≤—Å–µ –º–æ–º–µ–Ω—Ç—ã –≤—Ä–µ–º–µ–Ω–∏ –º–µ–∂–¥—É —ç—Ç–∏–º–∏ –ø—Ä–æ–º–µ–∂—É—Ç–∫–∞–º–∏ (–≤–∫–ª—é—á–∞—è –≥—Ä–∞–Ω–∏—Ü—ã)
–≤ —Ñ–æ—Ä–º–∞—Ç–µ hh:mm —Å –∏–Ω—Ç–µ—Ä–≤–∞–ª–æ–º –≤ 1 –º–∏–Ω—É—Ç—É, –∫–∞–∂–¥—ã–π –Ω–∞ –æ—Ç–¥–µ–ª—å–Ω–æ–π —Å—Ç—Ä–æ–∫–µ.

–§–æ—Ä–º–∞—Ç –≤—Ö–æ–¥–Ω—ã—Ö –¥–∞–Ω–Ω—ã—Ö
–ù–∞ –≤—Ö–æ–¥ –ø—Ä–æ–≥—Ä–∞–º–º–µ –ø–æ–¥–∞—é—Ç—Å—è —á–µ—Ç—ã—Ä–µ —Ü–µ–ª—ã—Ö —á–∏—Å–ª–∞ h1,m1,h2,m2, –∫–∞–∂–¥–æ–µ –Ω–∞ –æ—Ç–¥–µ–ª—å–Ω–æ–π —Å—Ç—Ä–æ–∫–µ.

–§–æ—Ä–º–∞—Ç –≤—ã—Ö–æ–¥–Ω—ã—Ö –¥–∞–Ω–Ω—ã—Ö
–ü—Ä–æ–≥—Ä–∞–º–º–∞ –¥–æ–ª–∂–Ω–∞ –≤—ã–≤–µ—Å—Ç–∏ –≤ —Ñ–æ—Ä–º–∞—Ç–µ hh:mm –≤—Å–µ –º–æ–º–µ–Ω—Ç—ã –≤—Ä–µ–º–µ–Ω–∏ –º–µ–∂–¥—É –Ω–∞—á–∞–ª–æ–º –∏ –∫–æ–Ω—Ü–æ–º –∑–∞–¥–∞–Ω–Ω–æ–≥–æ –ø—Ä–æ–º–µ–∂—É—Ç–∫–∞
–≤ —Å–æ–æ—Ç–≤–µ—Ç—Å—Ç–≤–∏–∏ —Å —É—Å–ª–æ–≤–∏–µ–º –∑–∞–¥–∞—á–∏.

–ü—Ä–∏–º–µ—á–∞–Ω–∏–µ. –ì–∞—Ä–∞–Ω—Ç–∏—Ä—É–µ—Ç—Å—è, —á—Ç–æ –≤—Ä–µ–º—è –Ω–∞—á–∞–ª–∞ —Å—Ç—Ä–æ–≥–æ –º–µ–Ω—å—à–µ –≤—Ä–µ–º–µ–Ω–∏ –∫–æ–Ω—Ü–∞, –ø–æ—ç—Ç–æ–º—É –ø–µ—Ä–µ—Ö–æ–¥–∞ —á–µ—Ä–µ–∑ 00:00 –Ω–µ –±—É–¥–µ—Ç.
"""

# –°—á–∏—Ç—ã–≤–∞–µ–º –≤—Ö–æ–¥–Ω—ã–µ –¥–∞–Ω–Ω—ã–µ
h1 = int(input())
m1 = int(input())
h2 = int(input())
m2 = int(input())

# –ü–µ—Ä–µ–≤–æ–¥–∏–º –Ω–∞—á–∞–ª—å–Ω–æ–µ –∏ –∫–æ–Ω–µ—á–Ω–æ–µ –≤—Ä–µ–º—è –≤ –º–∏–Ω—É—Ç—ã
start_minutes = h1 * 60 + m1
end_minutes = h2 * 60 + m2

# –ü—Ä–æ—Ö–æ–¥–∏–º –ø–æ –≤—Å–µ–º –º–∏–Ω—É—Ç–∞–º –≤ –ø—Ä–æ–º–µ–∂—É—Ç–∫–µ
for total_minutes in range(start_minutes, end_minutes + 1):
    # –ü–µ—Ä–µ–≤–æ–¥–∏–º –æ–±—â–µ–µ –∫–æ–ª–∏—á–µ—Å—Ç–≤–æ –º–∏–Ω—É—Ç –æ–±—Ä–∞—Ç–Ω–æ –≤ —á–∞—Å—ã –∏ –º–∏–Ω—É—Ç—ã
    hours = total_minutes // 60
    minutes = total_minutes % 60

    # –§–æ—Ä–º–∞—Ç–∏—Ä—É–µ–º –≤—ã–≤–æ–¥ —Å –≤–µ–¥—É—â–∏–º–∏ –Ω—É–ª—è–º–∏ (2 —Ü–∏—Ñ—Ä—ã –¥–ª—è —á–∞—Å–æ–≤ –∏ –º–∏–Ω—É—Ç)
    print(f"{hours:02d}:{minutes:02d}")
