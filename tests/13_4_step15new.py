"""
Математика округлит 🧠
Напишите функцию math_round_to_int(num), которая принимает положительное число с плавающей точкой num,
округляет его до целого по математическим правилам, и возвращает результат.

Примечание 1.
Математические правила округления: если дробная часть числа меньше 0.5,
то округляем его в меньшую сторону. В противном случае округляем число в большую сторону.
Например:

8.25 ≈ 8
8.5 ≈ 9
8.75 ≈ 9

Примечание 2.
Приведённый ниже код:

print(math_round_to_int(1.52))
print(math_round_to_int(0.459))
print(math_round_to_int(10.0))

должен выводить:

2
0
10

Примечание 3.
В Python не существует встроенных функций для округления чисел по математическим правилам. 😢
"""


def math_round_to_int(num):
    """
    Округляет положительное число с плавающей точкой до целого по математическим правилам:
    - если дробная часть < 0.5, округляем в меньшую сторону;
    - если дробная часть ≥ 0.5, округляем в большую сторону.

    Args:
        num (float): положительное число с плавающей точкой

    Returns:
        int: округлённое до целого число
    """
    # Выделяем целую часть числа
    integer_part = int(num)
    # Вычисляем дробную часть
    fractional_part = num - integer_part

    # Применяем математические правила округления
    if fractional_part < 0.5:
        return integer_part
    else:
        return integer_part + 1


print(math_round_to_int(1.52))
print(math_round_to_int(0.459))
print(math_round_to_int(10.0))
